// **1. Función que suma dos números**
// Esta función toma dos números como parámetros y retorna su suma.
export function suma(a, b) {
  return a + b;
}

// **2. Función que verifica si un número es par**
// Devuelve true si el número es par, false en caso contrario.
export function esPar(num) {
  return num % 2 === 0;
}

// **3. Función que devuelve un saludo personalizado**
// Genera un saludo utilizando el nombre proporcionado como argumento.
export function saludo(nombre) {
  return `Hola, ${nombre}!`;
}
